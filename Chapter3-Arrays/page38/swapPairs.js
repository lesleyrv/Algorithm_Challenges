//array swap pairs
function swapPairs(arr){
    var limit;
    arr.length % 2 !== 0 ? limit = arr.length - 1:limit = arr.length;

    for (var i = 0; i < limit; i+= 2){
        var temp = arr[i];
        arr[i] = arr[i+1];
        arr[i+1]=temp;
    }
    console.log (arr);
    return arr;
}

swapPairs([1,2,3,4])